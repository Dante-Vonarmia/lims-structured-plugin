import { createTargetDateInputHandler } from "../shared/target-date-input-handler.js";
import { createPreviewZoomBindings } from "./preview-zoom.js";
import { createTemplateNavigationBindings } from "./template-navigation.js";
import { createUploadDropBindings } from "./upload-drop.js";
import { createViewModeBindings } from "./view-mode.js";
import { createBatchFilterBindings } from "./batch-filter.js";
import { createQueueTableBindings } from "./queue-table.js";
import { createTargetFieldEventBindings } from "./target-field-events.js";
import { createWorkbenchInitBindings } from "./workbench-init.js";

export function createBindEventsFeature(deps = {}) {
  const {
    $, state,
    FILTER_BLANK_TOKEN,
    MULTI_EDIT_DISABLED_FIELD_KEYS,
    createEmptyFields,
    appendLog,
    applyIncompleteState,
    applyTargetFieldProblemStyles,
    buildCategoryMessage,
    buildGeneralCheckWysiwygData,
    clearPreprocessProgress,
    cleanBlockText,
    createQueueItem,
    ensureTemplateEditorSchema,
    exportAll,
    generateAllReady,
    generateItem,
    getActiveItem,
    getColumnFilterOptionEntries,
    getFilteredSortedQueue,
    getGenerateMode,
    getSelectedNormalItems,
    inferCategory,
    isSupportedFile,
    isTargetMultiEditMode,
    isTypingTarget,
    maybeCopyGeneralCheckForBlankTemplate,
    navigateActiveItem,
    parseTableRowsFromBlock,
    processAllPending,
    refreshActionButtons,
    refreshActiveRecognition,
    refreshTargetFieldFormBySelection,
    renderPreviews,
    renderQueue,
    renderSourceFieldList,
    renderSourcePreview,
    renderTargetFieldForm,
    renderTargetPreview,
    renderTemplateSelect,
    resolveBlankTemplateName,
    runExcelBatch,
    setLoading,
    setPreviewFullscreen,
    setPreviewPlaceholder,
    setRightViewMode,
    setSourceViewMode,
    setStatus,
    updateTaskTemplateInfoApi,
    updateTaskStatusApi,
    saveWorkspaceDraft,
    rememberFieldValueFromTarget,
    acceptSuggestionFromTarget,
    syncGenerateModeUiText,
    shiftDateText,
    triggerDownload,
    authorizeDownloadWindow,
    updateSelectedCountText,
    updateSourceDeviceNameText,
    validateItemForGeneration,
    extFromName,
  } = deps;
    const { handleTargetDateInput } = createTargetDateInputHandler({
      $,
      shiftDateText,
    });
    const { bindPreviewZoomOverlayEvents } = createPreviewZoomBindings({ $ });
    const {
      addFilesToQueue,
      bindUploadEvents,
      bindQueueLayoutAndDropEvents,
    } = createUploadDropBindings({
      $,
      state,
      isSupportedFile,
      extFromName,
      createQueueItem,
      renderQueue,
      renderTemplateSelect,
      setStatus,
      appendLog,
      updateTaskStatusApi,
      processAllPending,
    });
    const { bindViewModeEvents } = createViewModeBindings({
      $,
      state,
      getActiveItem,
      renderSourceFieldList,
      setSourceViewMode,
      setRightViewMode,
      renderSourcePreview,
    });
    const { bindTemplateAndNavigationEvents } = createTemplateNavigationBindings({
      $,
      state,
      getActiveItem,
      resolveBlankTemplateName,
      ensureTemplateEditorSchema,
      maybeCopyGeneralCheckForBlankTemplate,
      validateItemForGeneration,
      applyIncompleteState,
      renderQueue,
      renderTargetFieldForm,
      renderTargetPreview,
      renderTemplateSelect,
      setPreviewFullscreen,
      navigateActiveItem,
      isTypingTarget,
    });
    let blockDownloadUntil = 0;
    let downloadPointerArmed = false;
    const { bindBatchAndFilterEvents } = createBatchFilterBindings({
      $,
      state,
      appendLog,
      clearPreprocessProgress,
      exportAll,
      generateAllReady,
      generateItem,
      getActiveItem,
      getGenerateMode,
      getSelectedNormalItems,
      refreshActiveRecognition,
      renderPreviews,
      renderQueue,
      renderTemplateSelect,
      runExcelBatch,
      setLoading,
      setPreviewFullscreen,
      setPreviewPlaceholder,
      setRightViewMode,
      setStatus,
      triggerDownload,
      authorizeDownloadWindow,
      updateTaskStatusApi,
      getBlockDownloadUntil: () => blockDownloadUntil,
      setBlockDownloadUntil: (next) => { blockDownloadUntil = Number(next) || 0; },
      setDownloadPointerArmed: (next) => { downloadPointerArmed = !!next; },
      isDownloadPointerArmed: () => !!downloadPointerArmed,
    });
    const { bindQueueTableEvents } = createQueueTableBindings({
      $,
      state,
      FILTER_BLANK_TOKEN,
      getColumnFilterOptionEntries,
      getFilteredSortedQueue,
      getActiveItem,
      renderQueue,
      renderTemplateSelect,
      renderPreviews,
      renderSourceFieldList,
      renderSourcePreview,
      renderTargetPreview,
      updateSelectedCountText,
      refreshActionButtons,
      refreshTargetFieldFormBySelection,
      updateSourceDeviceNameText,
      setBlockDownloadUntil: (next) => { blockDownloadUntil = Number(next) || 0; },
    });
    const { bindTargetFieldEvents } = createTargetFieldEventBindings({
      $,
      state,
      MULTI_EDIT_DISABLED_FIELD_KEYS,
      createEmptyFields,
      applyIncompleteState,
      applyTargetFieldProblemStyles,
      buildCategoryMessage,
      buildGeneralCheckWysiwygData,
      cleanBlockText,
      getActiveItem,
      getGenerateMode,
      getSelectedNormalItems,
      inferCategory,
      isTargetMultiEditMode,
      maybeCopyGeneralCheckForBlankTemplate,
      parseTableRowsFromBlock,
      refreshTargetFieldFormBySelection,
      renderQueue,
      renderSourceFieldList,
      renderTargetFieldForm,
      saveWorkspaceDraft,
      setStatus,
      updateTaskTemplateInfoApi,
      validateItemForGeneration,
      handleTargetDateInput,
      rememberFieldValueFromTarget,
      acceptSuggestionFromTarget,
    });

    const { bindEvents } = createWorkbenchInitBindings({
      $,
      saveWorkspaceDraft,
      bindUploadEvents,
      bindQueueTableEvents,
      bindQueueLayoutAndDropEvents,
      bindViewModeEvents,
      bindPreviewZoomOverlayEvents,
      bindTargetFieldEvents,
      bindTemplateAndNavigationEvents,
      bindBatchAndFilterEvents,
    });

  return { bindEvents };
}
